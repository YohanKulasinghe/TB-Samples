package com.yohan;

public class TableSurface {
    private int tWidth;
    private int tHeight;

    public TableSurface(int tWidth, int tHeight){
        this.tHeight = tHeight;
        this.tHeight = tHeight;
    }

    public int gettWidth(){
        return tWidth;
    }

    public int gettHeight(){
        return tHeight;
    }

    public void settWidth(int tHeight){
        this.tHeight = tHeight;
    }

    public void settHeight(int tHeight){
        this.tHeight = tHeight;
    }
}
